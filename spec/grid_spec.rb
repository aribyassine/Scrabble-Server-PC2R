require 'rspec'
require_relative '../lib/pc2r/grid'
require_relative '../lib/pc2r/string'
require_relative '../lib/config'

describe 'Grid' do

  Pc2r::Config.load
  grid = Pc2r::Grid.new

  it 'should add first word <salut>' do
    word = 's00000000000000'+
        'a00000000000000'+
        'l00000000000000'+
        'u00000000000000'+
        't00000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'
    expect(grid.set! word).to eq 'salut'
    puts grid.to_s

  end
  it 'should add word <lutte>' do
    word = 's00000000000000'+
        'a00000000000000'+
        'lutte0000000000'+
        'u00000000000000'+
        't00000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'
    expect(grid.set! word).to eq 'lutte'
    puts grid.to_s
  end
  it 'should add word <lutte>' do
    word = 's00b00000000000'+
        'a00o00000000000'+
        'lutte0000000000'+
        'u00t00000000000'+
        't00e00000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'+
        '000000000000000'
    expect(grid.set! word).to eq 'botte'
    puts grid.to_s
  end
end